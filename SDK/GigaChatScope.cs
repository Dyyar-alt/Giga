namespace GigaChatSDK
{
    /// <summary>
    /// Области доступа (scopes) для GigaChat API.
    ///
    /// Определяет типы подписок и права доступа к функциям API.
    /// </summary>
    public enum GigaChatScope
    {
        /// <summary>
        /// Персональный доступ (физические лица).
        /// Бесплатный тариф с ограничениями.
        /// </summary>
        GIGACHAT_API_PERS,

        /// <summary>
        /// B2B доступ (ИП и юридические лица).
        /// Предоплатные пакеты с расширенными возможностями.
        /// Поддерживает обработку документов и расширенные функции.
        /// </summary>
        GIGACHAT_API_B2B,

        /// <summary>
        /// Корпоративный доступ (крупные компании).
        /// Постоплата (pay-as-you-go) с максимальными возможностями.
        /// </summary>
        GIGACHAT_API_CORP
    }
}
